# â”€â”€â”€ One-time heavyweight import â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
from app.services.llm_loader import model, tokenizer           # noqa: E402

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger("model_server")

# ------------------------------------------------------------------------
# Helper: generic â€œstop on these stringsâ€ criterion (works for any model)
# ------------------------------------------------------------------------
_DEFAULT_STOP_STRINGS = ["</s>", "</assistant>", "### End"]


class StopOnStrings(StoppingCriteria):
    """
    Abort generation once *any* of the given full **token sequences**
    has just appeared at the end of the output.
    """

    def __init__(self, stop_strings: Iterable[str]):
        super().__init__()
        self.seqs: List[List[int]] = [
            tokenizer.encode(s, add_special_tokens=False) for s in stop_strings
        ]

    def __call__(self, input_ids, scores, **kwargs) -> bool:  # noqa: D401
        for seq in self.seqs:
            if len(input_ids[0]) < len(seq):
                continue
            window = input_ids[0, -len(seq):].tolist()
            if window == seq:
                return True
        return False


# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ Servicer â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
class GeneratorServicer(pbr.GeneratorServicer):                # type: ignore
    """Implements the bidirectional *StreamGenerate* RPC."""

    async def StreamGenerate(                                  # noqa: D401
        self,
        request: pb.GenerateRequest,
        context: grpc.aio.ServicerContext,
    ):
        # â”€â”€â”€â”€â”€ build chat-template prompt â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        messages = [
            {
                "role": "system",
                "content": "You are a helpful assistant.",
            },
            {
                "role": "user",
                "content": request.user_content,
            },
        ]

        input_ids = tokenizer.apply_chat_template(
            messages,
            tokenize=True,
            add_generation_prompt=True,   # adds the "<assistant>" prefix
            return_tensors="pt",
        ).to(model.device)

        logger.info("â©  Received user content (%d chars)", len(request.user_content))

        # â”€â”€â”€â”€â”€ streamer converts token IDs â†’ text chunks â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        streamer = TextIteratorStreamer(
            tokenizer,
            skip_prompt=True,
            skip_special_tokens=True,
            decode_kwargs={"skip_special_tokens": True},
        )

        # â”€â”€â”€â”€â”€ generation parameters â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        max_new     = request.max_new_tokens or int(os.getenv("MAX_TOKENS", "256"))
        temperature = request.temperature      or 0.7
        top_p       = request.top_p            or 0.9

        stop_strings = (
            os.getenv("STOP_STRINGS", "|".join(_DEFAULT_STOP_STRINGS)).split("|")
        )
        stopping = StoppingCriteriaList([StopOnStrings(stop_strings)])

        # â”€â”€â”€â”€â”€ run blocking generate() in a background thread â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        def _run_generation() -> None:
            model.generate(
                input_ids=input_ids,
                streamer=streamer,
                generation_config=GenerationConfig(
                    max_new_tokens=max_new,
                    do_sample=True,
                    temperature=temperature,
                    top_p=top_p,
                    eos_token_id=tokenizer.eos_token_id,
                    pad_token_id=tokenizer.eos_token_id,
                ),
                stopping_criteria=stopping,
            )

        threading.Thread(target=_run_generation, daemon=True).start()

        # â”€â”€â”€â”€â”€ yield tokens as they appear â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        for token in streamer:
            yield pb.GenerateChunk(text=token)


# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ Server loop â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
async def serve() -> None:
    server = grpc.aio.server()
    pbr.add_GeneratorServicer_to_server(GeneratorServicer(), server)        # type: ignore
    listen_addr = "[::]:50051"
    server.add_insecure_port(listen_addr)
    logger.info("ğŸš€  LLM gRPC server listening on %s", listen_addr)

    await server.start()
    await server.wait_for_termination()


if __name__ == "__main__":
    # Make sure repo-root is on PYTHONPATH when run directly
    root = Path(__file__).resolve().parents[2]
    import sys

    if str(root) not in sys.path:
        sys.path.insert(0, str(root))

    asyncio.run(serve())
